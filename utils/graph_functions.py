import networkx as nx

# %%GRAPH
# ======================
def create_graph():
    G = nx.Graph()

    # node layout
    # 0,1: start
    # 2,3: hub
    # 4,5: destination
    # 6-11: intermediate
    G.add_nodes_from(range(22))

    # paths S -> H
    paths_SH = {
        (0,2): [[0,6,2], [0,7,2]],
        (0,3): [[0,10,3], [0,11,3]],
        (1,2): [[1,18,2], [1,19,2]],
        (1,3): [[1,14,3], [1,15,3]]
    }

    # paths H -> D
    paths_HD = {
        (2,4): [[2,8,4], [2,9,4]],
        (2,5): [[2,12,5], [2,13,5]],
        (3,4): [[3,20,4], [3,21,4]],
        (3,5): [[3,16,5], [3,17,5]]
    }

    def add_paths_with_fixed_weights():
        edge_weights = {
            # S -> H
            (0,6): 3.0, (6,2): 3.0,
            (0,7): 4.0, (7,2): 3.0,
            (0,10): 4.0, (10,3): 3.0,
            (0,11): 4.0, (11,3): 4.0,
    
            (1,18): 4.0, (18,2): 3.0,
            (1,19): 5.0, (19,2): 3.0,
            (1,14): 3.0, (14,3): 3.0,
            (1,15): 4.0, (15,3): 3.0,
    
            # H -> D
            (2,8): 4.0, (8,4): 3.0,
            (2,9): 4.0, (9,4): 4.0,
            (2,12): 4.0, (12,5): 4.0,
            (2,13): 4.0, (13,5): 5.0,
    
            (3,20): 4.0, (20,4): 4.0,
            (3,21): 5.0, (21,4): 4.0,
            (3,16): 4.0, (16,5): 3.0,
            (3,17): 4.0, (17,5): 4.0
        }
    
        for (u, v), w in edge_weights.items():
            G.add_edge(u, v, weight=w)
    add_paths_with_fixed_weights()
    return G, paths_SH, paths_HD